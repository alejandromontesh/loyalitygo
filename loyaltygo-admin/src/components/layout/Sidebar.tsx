export function Sidebar({ collapsed, onToggle }: SidebarProps) {
  const { user } = useAuth();

  const navItems = navigation.filter((item) => user && item.roles.includes(user.role as Role));

  return (
    <>
      {!collapsed && (
        <div className="fixed inset-0 z-30 bg-black/50 lg:hidden" onClick={onToggle} />
      )}
      <aside
        className={`fixed left-0 top-0 z-40 flex h-full flex-col bg-slate-900 transition-all duration-300 dark:bg-slate-950 lg:static lg:z-auto ${
          collapsed ? 'w-0 -translate-x-full overflow-hidden lg:w-16 lg:translate-x-0' : 'w-64'
        }`}
      >
        <div className="flex h-16 items-center gap-3 border-b border-slate-800 px-4">
          <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-indigo-600 text-sm font-bold text-white">
            LG
          </div>
          {!collapsed && (
            <span className="text-lg font-semibold text-white">LoyalitiGo</span>
          )}
        </div>

        <nav className="flex-1 space-y-1 overflow-y-auto p-3">
          {navItems.map((item) => (
            <NavLink
              key={item.path}
              to={item.path}
              end={item.path === '/'}
              onClick={() => window.innerWidth < 1024 && onToggle()}
              className={({ isActive }) =>
                `flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors ${
                  collapsed ? 'justify-center' : ''
                } ${
                  isActive
                    ? 'bg-indigo-600 text-white'
                    : 'text-slate-400 hover:bg-slate-800 hover:text-white'
                }`
              }
            >
              <span dangerouslySetInnerHTML={{ __html: icons[item.icon] || '' }} />
              {!collapsed && <span>{item.label}</span>}
            </NavLink>
          ))}
        </nav>
      </aside>
    </>
  );
}
